    {

        Route::post('/user/subscribe', function (Request $request) {
            $request->user()->newSubscription(
                'default', 'price_monthly'
            )->create($request->paymentMethodId);
        });

        
        $intent = Auth::user()->createSetupIntent();

        // サブスクリプション登録画面へ遷移
